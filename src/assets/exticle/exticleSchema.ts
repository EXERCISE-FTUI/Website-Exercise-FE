class Exticle{
    title: string;
    description: string;
    image: string;
    author: string;
    writtenOn: string;
    
    constructor(title: string, description: string, image: string, author: string, writtenOn: string) {
        this.title = title;
        this.description = description;
        this.image = image;
        this.author = author;
        this.writtenOn = writtenOn;
    }
}

export default Exticle;